package objetQuiDessine;
import java.awt.Color;

public class CurseurQuiDessine implements ObjetQuiDessine{
	
	private int direction;
	private int abscisse, ordonnee;
	private Color couleur;
	private boolean pendown;
	private int pensize; 
	private int actuabscisse, actuordonnee;
	
	public CurseurQuiDessine(){
		this.abscisse=0;
		this.ordonnee=0; 
		this.pensize=1;
		this.couleur=Color.BLACK;
		this.setPendown(true);
		
	}
	
	public int getDirection() {
		return direction;
	}

	public void setDirection(int direction) {
		this.direction = direction;
	}

	public int getAbscisse() {
		return abscisse;
	}

	public void setAbscisse(int abscisse) {
		this.abscisse = abscisse;
	}

	public int getOrdonnee() {
		return ordonnee;
	}

	public void setOrdonnee(int ordonnee) {
		this.ordonnee = ordonnee;
	}

	public Color getCouleur() {
		return couleur;
	}

	public void setCouleur(Color couleur) {
		this.couleur = couleur;
	}

	public int getPensize() {
		return pensize;
	}

	public void setPensize(int pensize) {
		this.pensize = pensize;
	}

	public boolean isPendown() {
		return pendown;
	}

	public void setPendown(boolean pendown) {
		this.pendown = pendown;
	}

	public int getActuabscisse() {
		return actuabscisse;
	}

	public void setActuabscisse(int actuabscisse) {
		this.actuabscisse = actuabscisse;
	}

	public int getActuordonnee() {
		return actuordonnee;
	}

	public void setActuordonnee(int actuordonnee) {
		this.actuordonnee = actuordonnee;
	}
}
